// Number 1
const n=153;

// The sum of first n numbers where n is a variable.
let i=0;
let sum=0;
for (i=0;i<=n;i++){
    sum+=i;
}
console.log("Number is: ",n);
console.log("The sum of first n numbers is: ",sum); 


// table of n where n is a variable.
console.log("Table of number ",n);

for(i=1;i<=10;i++){
    console.log(n, "X", i, "=", n*i);
}

// Check if it is a prime number.

let isPrime=true;
for(let i=2;i*i<=n;i++){
    if(n%i==0){
        isPrime=false;
        break;
    }
}

if(isPrime){
    console.log(n+" is a prime number");
}
else{
    console.log(n+" is not a prime number");
}

// Factors of n
console.log("Factors of number ",n, "are: ");

for(let i=1;i<=n;i++){
    if(n%i==0){
        console.log(i);
    }
}

// Sum of digits
let temp1=n;
sum=0;
while (temp1>0){
    sum+=temp1%10;
    temp1=Math.floor(temp1/10)
}

console.log("Sum of all the digits of number", n, "is: ",sum);

// Armstrong

let temp=n;
sum=0;
while (temp>0){
    let digit=temp%10;
    sum+=digit**3;
    temp=Math.floor(temp/10);
}

if (sum==n){
    console.log(n, " is an armstrong number");
}
else{
    console.log(n, " is not an armstrong number");
    
}

